                        <p>Rangkaian dihari pertama telah selesai, kini berlanjut pada hari kedua dimana diadakan persembahyangan bersama di gedung kampus masing-masing. dilakukan persembahyangan bersama bagi mahasiswa angkatan 2022.</p>
                        <p>Acara pertama dibuka dengan tari Rejang Sari oleh UKM Tari dan para dosen maupun staff  yang ditampilkan sebelum acara persembahyangan dimulai. Makna tari rejang secara keseluruhan melambangkan kesejahteraan, kemakmuran, keselamatan, dan kesucian masyarakat hindu serta sebagai bentuk pujian terhadap Dewa. Sedangkan makna tari rejang sari melambangkan sari atau bunga yang terkandung dalam bentuk gerak, tata busana, serta gelungan atau hiasan pada kepala penari.</p>
                        <p>Setelah tari rejang sari usai, acara dilanjut dengan penyucian, seluruh warga kampus terlebih dahulu dibersihkan secara niskala dengan tirta suci dan ngayab banten. Kemudian acara dilanjutkan dengan upacara persembahyangan Saraswati yang dipimpin oleh Pemangku. Persembahyangan terdiri dari Puja Tri Sandhya dan panca sembah. Setelah persembahyangan usai, para dosen membantu membagikan tirta ke seluruh mahasiswa. Acara persembahyangan diakhiri dengan parama santhi. Kemudian seluruh mahasiswa dapat nunas tirta untuk dibawa pulang dan ngelungsur gebogan masing-masing kelas yang nantinya akan dinikmati bersama-sama. Demikianlah rangkaian acara hari Saraswati yang dilaksanakan di Institut Design Bali dan New Media College.</p>
