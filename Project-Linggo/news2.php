                        <p>Denpasar, 21/10/2022—Dalam rangka menyambut hari raya besar umat hindu yaitu Hari Saraswati, maka New media college bali mengadakan acara perlombaan serta persiapan penjor dan gebogan menjelang hari Saraswati yang jatuh pada tanggal 22 Oktober 2022. Pada hari saraswati dilaksanakan persembahyangan bersama bagi umat beragama hindu.</p>
                        <p>Rangkaian acara ini tentu saja disambut dengan antusias oleh warga kampus yang mayoritas adalah umat hindu. Hal ini terlihat dari antusiasme mereka dalam mengikuti rangkaian perlombaan yang diadakan di gedung kampus IDB, Renon, Denpasar.</p>
                        <p>Hal ini disambut baik oleh para mahasiswa yang terlihat bersemangat dalam membuat penjor dan gebogan, mereka tak tanggung-tanggung, para mahasiswa ini rela mengeluarkan uang yang tidak sedikit. Dari hasil wawancara yang dilakukan oleh tim jurnalistik, kisaran 200 hingga 300 ribu mereka keluarkan untuk membeli bahan-bahan perlombaan. Tentu ini bukan angka yang sedikit, namun setiap mahasiswa di kelas rela untuk menyumbang secara sukarela sebagai bukti antusiasme dan rasa tulus ikhlas dalam mempersembahkan yadnya.</p>
                        <p> lomba gebogan adalah lomba menyusun buah sebagai persembahan di hari raya saraswati nanti. Hasil dari lomba pejati dan gebogan ini akan digunakan untuk keesokan harinya saat persembahyangan bersama. Buah-buahan yang digunakan dalam lomba gebogan ini adalah buah lokal, rata-rata menggunakan pisang dan juga buah naga. Sementara untuk jajan yang digunakan adalah bolu dan cupcake yang disusun secara rapih. Tak lupa juga hiasan dari daun lontar yang mempercantik buah yang disusun dalam gebogan.</p>
